export function toN(num) {
  if (num === undefined || num === null) return '';
  return String(num).replace(/[0-9]/g, d => '०१२३४५६७८९'[d]);
}

function bsMonthDays(year, idx0) {
  if (BS_DATA[year]) return BS_DATA[year][idx0];
  return 30;
}

function bsYearDays(year) {
  if (BS_DATA[year]) {
    let s = 0;
    for (let j = 0; j < 12; j++) s += BS_DATA[year][j];
    return s;
  }
  return 365;
}

export function bs2ad(y, m, d) {
  let t = 0;
  if (y >= BS_START) {
    for (let i = BS_START; i < y; i++) t += bsYearDays(i);
  } else {
    for (let i = y; i < BS_START; i++) t -= bsYearDays(i);
  }
  for (let j = 0; j < m - 1; j++) t += bsMonthDays(y, j);
  t += d - 1;
  const r = new Date(BASE_AD);
  r.setDate(r.getDate() + t);
  return r;
}

export function ad2bs(dt) {
  const tgt = new Date(dt.getFullYear(), dt.getMonth(), dt.getDate());
  const df = Math.round((tgt - BASE_AD) / 864e5);
  let y = BS_START;
  if (df >= 0) {
    while (true) {
      const yd = bsYearDays(y);
      if (df < yd) break;
      df -= yd;
      y++;
      if (y > BS_MAX_YEAR + 100) break;
    }
  } else {
    while (df < 0) {
      y--;
      df += bsYearDays(y);
      if (y < BS_MIN_YEAR - 100) break;
    }
  }
  let m = 1;
  for (let i = 0; i < 12; i++) {
    const md = bsMonthDays(y, i);
    if (df < md) { m = i + 1; break; }
    df -= md;
  }
  return { y, m, d: df + 1 };
}

export function getMaxDay(year, month) {
  return bsMonthDays(year, month - 1);
}
