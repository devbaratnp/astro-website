function populateDaySelect(maxDay, selected) {
    daySel.innerHTML = '';
    for (var i = 1; i <= maxDay; i++) {
        var opt = document.createElement('option');
        opt.value = i;
        opt.textContent = toN(i);
        if (selected && i === selected) opt.selected = true;
        daySel.appendChild(opt);
    }
}

